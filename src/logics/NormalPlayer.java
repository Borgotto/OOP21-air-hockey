package logics;

/**
 * The class NormalPlayer represents a user-controlled player.
 */
public class NormalPlayer extends Player {
    /**
     * This is the constructor for NormalPlayer. It behaves pretty much the same as its superclass.
     * @param posX The player's position on the X-axis.
     * @param posY The player's position on the Y-axis.
     * @param name The player's name.
     */
    public NormalPlayer(int posX, int posY, String name) {
        super(posX, posY, name);
    }
}
